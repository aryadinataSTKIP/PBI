function toggleDonation() {
    var donationContainer = document.getElementById("donation-container");
    if (donationContainer.style.display === "none" || donationContainer.style.display === "") {
        donationContainer.style.display = "block"; // Change to "flex" to center the content
    } else {
        donationContainer.style.display = "none";
    }
}

function showDonationPage() {
    document.getElementById('donation-container').style.display = 'flex';
}

function hideDonationPage() {
    document.getElementById('donation-container').style.display = 'none';
}

